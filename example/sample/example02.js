function mul(p1, p2) {
    return p1 * p2;
}

function add(p1, p2) {
    return p1 + p2;
}

function sub(p1, p2) {
    return p1 - p2;
}

function div(p1, p2) {
    return p1 / p2;
}

var resultMul = mul(5, 5);
console.log(resultMul);

var resultAdd = add(5, 5);
console.log(resultAdd);

var resultSub = sub(5, 5);
console.log(resultSub);

var resultDiv = div(5, 5);
console.log(resultDiv);
